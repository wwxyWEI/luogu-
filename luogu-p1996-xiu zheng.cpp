#include <iostream>

using namespace std;
const int N = 1000200;
// 数组大小：因为约瑟夫问题中反复将队头元素移到队尾，tt 会不断增大，
// 最大可能达到 n + n*(m-1) 左右，保险起见开到 1e5 以上，避免越界。
int q[N];

int main()
{
    int n,m;
    cin >> n >> m;
    int hh = 0,tt = -1;
    for (int i=1;i<=n;i++)//将1到n的整数依次入队，使用q数组来存储队列中的元素，hh表示队头下标，tt表示队尾下标，初始时队列为空，所以hh为0，tt为-1
    {
        q[++tt] = i;
    }

    while(hh<=tt)//当队列不为空时，继续进行操作，判断条件是hh小于等于tt，如果hh大于tt，说明队列中没有元素了，循环结束
    {
        for(int i=1;i<m;i++)
    {
        q[++tt] = q[hh];//将队头元素重新入队，模拟将队头元素移动到队尾的操作，先将tt加1，然后将q[hh]的值存储在q[tt]位置
        hh++;//将队头元素出队，直接将hh加1，表示队头元素被弹出，注意这里没有实际删除元素，只是通过hh的变化来模拟队列的行为
    }
    cout << q[hh] << " ";//输出当前队头元素
    hh++;//将当前队头元素出队，直接将hh加1，表示队头元素被弹出
    }
    return 0;
}